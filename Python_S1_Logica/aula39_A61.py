"""
Operadores de atribuição
= += -= *= /= //= **= %=
"""

contador = 100

##########

contador += 2  # Soma
print(contador)

contador -= 4  # Subtração
print(contador)

contador *= 2  # Multiplicação
print(contador)

contador /= 2  # Divisão
print(contador)

contador //= 2  # Divisão inteira
print(contador)

contador **= 2  # Potenciação
print(contador)

contador %= 2  # Módulo
print(contador)
