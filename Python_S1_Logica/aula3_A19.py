# Tipo String

"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Rafael Alves')
print(1, 'Rafael "Alves"')


# Aspas duplas
print("Rafael Alves")
print(2, "Rafael 'Alves'")


# Escape
print("Rafael \"Alves\"")

# r
print(r"Rafael \"Alves\"")
